// Pct //////////////////////////////////////////////////////////
const fgPct = (stats, playerAmount) => {
  const dataFgPct = {
    labels: stats
      .filter(stat => stat.fga_total >= 50)
      .sort((a, b) => b.fg_pct - a.fg_pct)
      .slice(0, playerAmount)
      .map(stat => stat.name),
    datasets: [{
      label: 'Fg %',
      data: stats
        .filter(stat => stat.fga_total >= 50)
        .sort((a, b) => b.fg_pct - a.fg_pct)
        .slice(0, playerAmount)
        .map(stat => stat.fg_pct),
      backgroundColor: backgroundColor,
      hoverOffset: 4
    }]
  }
  return dataFgPct
}

